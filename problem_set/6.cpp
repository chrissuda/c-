#include <iostream>
using namespace std;

int main()
{
    int length;
    int width;
    cout <<"Input the length of the rectangle:";
    cin>>length;
    cout<<"Input the length of the rectangle:";
    cin>>width;
    cout<<"The area of the rectangle is: "
    	<<width*length<<endl;
    cout<<"The perimeter of the rectangle is: "
    	<<width+length<<endl;
    return 0;
}